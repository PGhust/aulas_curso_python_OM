# Introdução a tipos de dados: Strings!

'''
Python é uma linguagem de programação que possue uma tipagem dinâmica / forte, ou seja, já sabe o tipo de informação e não preciso esécificar, por exemplo, o que são números. 
str -> string -> texto
string são textos que estão dentro (contidos) em aspas ""
'''

# Aspas simples

print('argumento')

# Aspas duplas

print("argumento")

# Escape: pula a próxima informação a ser interpretada

print("argu\"mento")

print('argu\"mento\"') # Abrindo e fechando com '' works
print("argu\'mento\'") # Abrindo e fechando com "" works

print("argu\"mento\"")

#r -> exibe o caracter escondido de barra invertida "\"

print(r"argu\"mento\"") # Utilizado para expressões regulares