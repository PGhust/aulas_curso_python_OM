# Int e Float

#Int -> Qualque número inteiro positivo ou negativo 

print(1)
print(-1)
print(0) #Número literal inteiro 


#Float -> Número com ponto flutuante, representa qualquer número com ponto flutuante, sem sinal é considerado positivo 

print(1.1)
print(0.0)
print(-1.4)

# A função type mostra o tipo que o Python inferiu ao valor 

print( type('AAA'))
print( type(1))
print( type(-1), type(0.0), type(12) )